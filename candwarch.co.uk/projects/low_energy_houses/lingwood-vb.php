      <td align="left" valign="top"><div id="copy"><!-- InstanceBeginEditable name="copy" -->This example of contemporary architecture is located on a spectacular site overlooking the Solway Firth. The site is a steeply sloping, former quarry in a National Scenic Area.
            <p>The first floor forms the public living accommodation with external terrace facing the sea, the ground floor contains the private bedroom and bathroom facilities and the lower floor contains the garage and main entrance. The base of the quarry is the only flat part of the site and this is used for car access and the entrance to the house. </p>
            <p>The external expression of this example of contemporary architecture is based on the following strategy. The public first floor is conceived as a glazed pavilion sitting on top of a masonry base, which contains the private accommodation. The pavilion is constructed with a standing seam zinc roof, glazed on the sides with views and panels of durable timber cladding. The masonry base is clad with natural stone matching the exposed rock face of the quarry.</p>
            <p>The house has been future proofed with the following low energy features;</p>
            <p>	1. The external walls, floor and roof are insulated to a high standard and air infiltration is minimised.<br />
              2. Triple glazed windows with warm edge spacer bars, thermally broken frames and inert gas filled to achieve a whole window u-value of 0.7W/m2K.<br />
              3. Heat pump using a borehole as the ground source for the underfloor heating and hot water system with a closed combustion woodburning stove as back up. <br />
              4. Micro generation of renewable electricity using roof mounted Photovoltaic Panels.<br />
              5. Whole house heat recovery ventilation system.</p><p>Recent articles publishing the building are:<br />
