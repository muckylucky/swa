        <p>Locharthur, part of the <a href="http://www.locharthur.org.uk/">Camphill Village Trust</a>, is a community living and working with people with a range of learning disabilities, mental and physical health problems. Nine people - both carers and those with special needs - live together in the house, sharing their lives.</p>
        <p>The house was designed in collaboration with Denis Chanarin, an architect and community member. It is based on some of the precepts of Rudolf Steiner whose philosophy is fundamental to the community. The forms and spaces are gently non-rectilinear.</p>
        <p>The house is sited on a gentle rise overlooking Loch Arthur and Lotus Hill. The entrance embraces two mature oak trees. The principal living areas enjoy the southerly view of the loch with the bedroom wings having distant views over farmland to the west. The plan is arranged to take advantage of the views and to create a series of semi-sheltered outdoor “rooms”. The single storey organisation makes for ease of access with minimum interruption from fire doors. The extending wings and the shape of the slate roofs respond to the natural fall of the ground and the surrounding landscape in an organic way.</p>
        <p>The structure is a highly insulated timber frame comprising part traditional timber framed wall panels and part a frame structure of timber posts, trusses and purlins. Natural materials are used and expressed wherever appropriate.</p>
