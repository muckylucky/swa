        <p>This development comprises the demolition 		of the existing single storey house and the erection of a replacement 		dwelling house which will be two storeys with a basement. The existing 		house does not fulfil the client's needs in terms of size, design and 		situation within the plot.</p>
        <p>	Whilst the new house will be larger than the existing house, it has been 		designed to be of an appropriate scale and form in relation to the 
          surrounding properties. In terms of residential amenity, the 
          rebuilding of the house will facilitate the repositioning of the building 		in order to achieve an enlarged and more useable rear garden area 
          by placing the new house closer to the street. This will maximise the 		sunny southerly aspect of the rear of the property. In addition to this 		benefit, the repositioning of the new house within the plot will create 
          a building line which is more consistent with the adjacent properties. 		The new house faces the view to the north west, the previous house 		addressed the south west to a view which is now obscured by trees.</p>
        <p>The strategy to using the site slope is to excavate the outcrop on which 		the current house sits to form an entrance and garage at the lower 		ground floor. The ground floor is the principal floor and opens out 		directly to the south facing garden to the rear of the property. The top 		floor is set back to reduce the scale of the property when viewed from 		the street and to also provide a roof terrace facing the northerly distant 		landscape views. </p>
        <p>The external materials which will be used in construction will be of a 		high quality, commensurate with this design approach, consisting 
          of pre weathered, grey standing seam zinc roofing to the roofs, with 		matching zinc gutters and downpipes. Durable timber cladding is used 		to the walls and natural stone cladding to the lower ground floor. The 		windows and doors will be high performance, timber or aluminium 		framed with aluminium cladding. The fabric of the house will be highly insulated and constructed to 
          minimise air infiltration. </p>
