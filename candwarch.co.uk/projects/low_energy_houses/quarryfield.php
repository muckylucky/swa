        <p>This single story house and garage, currently under construction, have been located towards the north eastern end
          of the site aligned with the existing contours and over the approximate footprint of a
          previous dwelling. The floor level of the proposed building is close to the existing ground
          level to minimise underbuilding and to give a close fit with the landscape.
        <p>The proposed access road from the existing unclassified road is kept to a minimum
          length and, like the house, follows the contours of the site to achieve a natural fit with
          the land. It is given a gentle S shape to mimic the flowing nature of the surrounding
          landscape.</p>
        <p>Other than vehicle access and integral terraces on three sides of the house the site is
          given over to soft landscape comprising a boundary hedge, areas of rough grass with
          wild flowers and bulbs, naturalistic groups of native species planting and an area of
          mown grass with generous flowing lines.
          The overall aim is integrate the house and access road with the existing lie of the land
          and make a soft landscape setting which is rural in character.</p>
        <p>The design is treated as a small cluster of forms to break up the apparent size, and to provide outdoor areas that are sheltered from different wind directions. The different elements are emphasised by changing the direction of the cedar cladding. The glazed openings are designed to take full advantage of the distant views.<br />
          The entrance to the house is on the north west side of the house under the cover of the roof to provide shelter from the prevailing wind. The principal rooms are situated to take advantage of the views to the south and east. The house sits on a fairly level plateau so minimal excavation will be needed.</p>
