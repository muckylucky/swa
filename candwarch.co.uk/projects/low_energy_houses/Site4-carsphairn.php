        <p>This proposed house is contemporary in appearance and comprises two
          single storey elements which are offset from each other in order to reduce
          their apparent size.
          The slope of the roof echoes the angle of the slope of the hill and maintains
          a low profile to the building frontage.
          The house is sited at a point on the hill where it is largely cut into the slope
          and is screened from both the north and the south by rising ‘shoulders’ of<br />
          land. Tree planting to the rear of the house and additional planting on the
          access track will help to integrate the house into the landscape setting.</p>
        <p><br />
            The house is clad in natural stone which is re-cycled quarry waste and a
              very close match to the stone on the hill. Pre-patinated grey zinc would be used to clad the roof &amp; some of the
              vertical panels above the stone. The materials are chosen to blend with
              their natural surroundings and to be recessive in the landscape as well as<br />
            durable to minimise maintenance requirements.
            This also fits with our aim to specify natural materials from appropriate
            sustainable sources, to reduce the overall carbon footprint of the building,
          both during its construction and during its life span. </p>
        <p>The design will make use of the site’s potential for generation of renewable
          energy. A wind turbine is proposed to generate all the building’s electrical
          energy requirements with the excess energy fed back to the grid. Also,<br />
          thermal solar panels can be used to minimise hot water requirements.</p>
