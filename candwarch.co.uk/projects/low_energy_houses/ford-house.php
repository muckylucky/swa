        <p>This new house nearing completion for two artists replaces an existing traditional farmhouse with a very low energy building clad in black zinc sheeting. The house comprises three distinct elements - main living accommodation, studio/guest wing, and garage.</p>
        <p>The use of black cladding is designed to echo the sheet metal clad agricultural barns nearby and to be visually recessive. Some windows are picked out in strong acid colours to contrast with the black zinc.</p>
        <p>The house is insulated to Passive House Standards and is heated using geothermal heat pump, solar panels and a whole house heat recovery ventilation system. </p>
