              2. A new dining area to the rear looking onto the garden and connected to the kitchen.<br />
              3. At the intersection of the above elements, a top-lit study and set of steps resolve the junction between the higher garden level with the lower main house level.<br />
              4. External terrace and landscaping works.<br />
              5. Addition of en suite bathroom and new family bathroom.<br />
              6. Various other minor internal alternations and the addition of a new heating &amp; hot water system.<br />
