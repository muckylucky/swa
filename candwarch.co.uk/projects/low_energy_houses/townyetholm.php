        <p>This zero carbon new house in Yetholm, the &quot;place at the gate&quot;, a stone's throw from the English Border, will feature high levels of insulation, a ground source heat pump, photovoltaic panels and a whole house heat recovery ventilation system. Being within the conservation area, the house takes reference from traditional forms and materials, incorporating natural slate and locally sourced building stone, while adopting a contemporary design approach. A key feature is the open plan living / dining / kitchen space with large glazed openings to take advantage of views and daylight, linked via a zinc roofed circulation zone to subsidiary wings housing the bedrooms and garage / utility areas respectively. Breaking this substantial house down into a series of simple forms of appropriate scale and proportions helped to &quot;bed down&quot; the proposal into its context. </p>
        <p>Despite the clients being based overseas, we have been able to work closely with them to ensure the design meets their needs and involve them in every step of the decision making process, from specifying bathtubs to choosing door handles, thus proving that distance is not an object when it comes to working with an architect. </p>
