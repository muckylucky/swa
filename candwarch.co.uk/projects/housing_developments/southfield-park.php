      <td align="left" valign="top"><div id="copy"><!-- InstanceBeginEditable name="copy" -->This housing development is located on the former showfield, in the heart of Wigtown. The developer clients brief was to provide a mix of quality family houses together with the provision of a new public park.
            <p>The proposals were fully and extensively considered and formed part of an overall strategy for future housing and infrastructure development for the whole town. This comprehensive Masterplan approach, demonstrated the appropriateness and sensitivity of the proposals. </p>
            <p>The housing will have significant sustainability features;</p>
            <p>1. Energy efficient concrete floor slabs<br />
              2. Energy efficient closed panel timber frame system<br />
              3. High performance envelope to reduce heat loss and air infiltration<br />
              4. High performance windows <br />
              5. Mechanical ventilation with heat recovery<br />
              6. Photo-voltaic solar panels<br />
              7. Ground source heat pumps where possible<br />
              8. Rainwater harvesting<br />
              9. Low energy lighting, efficient appliances and smart metering<br />
              10. Passive solar design and use of sun spaces<br />
              11. Recycling facilities<br />
              12. Local renewable energy where possible<br />
              13. Landscaping features such as porous paving, soakaways and raised planting beds</p>
            <p>A substantial portion of the development is affordable. </p>
            <p>The affordable housing will benefit from many of the sustainability features described above. In particular the energy efficiency features are designed to reduce heating bills and help prevent fuel poverty.</p>
            <p>The affordable houses have a compact, semi detached form to minimise external wall area and heat loss. </p>
            <p>The building fabric (walls, floors, roof, external windows and doors) is super insulated and sealed to minimise air infiltration. The heat source is from an air source heat pump with wood burning stove as back up. Whole house ventilation system with heat recovery. Low energy lighting, efficient appliances and smart metering.</p>
