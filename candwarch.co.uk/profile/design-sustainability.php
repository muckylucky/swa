        <p>Practice Ethos<br />
          As architects we are passionate about the places we live and work in. Through our work and projects we strive to enhance peoples emotional and physical responses to the places where they live, work and socialise.  We aim to lift people’s spirits through the enjoyment of a place while making that place protective, responsive and environmentally sound.</p>
        <p>High Quality Design <br />
          We take pride in providing a high quality professional service and design with care and sensitivity to the character of the existing architecture, surrounding context and environment. We believe that a balanced approach is best which incorporates traditional building methods with contemporary ideas and that better standards deliver superior buildings which, together with considered and integrated landscaping, contribute to creating successful sustainable places.</p>
        <p>Low Carbon / Low Capital Cost<br />
          We have developed strategies in our previous projects to allow Low Carbon Buildings to be procured at levels of low capital outlay. We can advise on systems considered appropriate. We can undertake Energy Audit’s during the design process to determine the best solutions while also looking at the financial costs and returns on investment. Some features also qualify for grants and funding assistance.</p>
        <p>Local Climate, Global Responsibility<br />
          Our practice has worked extensively in South West Scotland and further afield in Scotland for over 25 years. This experience has informed an innate knowledge of the local climate and exposure, particularly in coastal areas. This knowledge establishes the necessary requirements of the building fabric that are needed to keep out the weather over the long term, whilst providing pleasant and easily maintained interior comfort levels.  We believe that using this depth of knowledge in tandem with our principles of sustainable design put us in a stronger position to deliver successful buildings. </p>
        <p>A Holistic Approach<br />
  A sustainable approach to the design of a building should be holistic and integrated. Proposals can provide a building with a mixture of sensible features either passive (eg insulation, orientation to the sun) or active (eg air or ground source heat pumps &amp; heat recovery ventilation). </p>
        <p>Sustainably Sourced<br />
          We aim to specify natural materials from appropriate sustainable sources, to reduce the overall carbon footprint of the building, both during its construction and together with the energy in use strategies described above, during its life span. Natural, durable external and internal finishes would be specified to minimize maintenance requirements.</p>
        <p>Insulation, Thermal Mass &amp; Air tightness<br />
          The primary strategy we would recommend would be to super insulate the external building fabric in conjunction with high thermal mass while detailing for the most air tight construction possible. This will minimize both heat loss and heat gain to reduce winter heating and summer cooling requirements.</p>
        <p>Renewable Energy<br />
          Wherever possible we try to encourage the use of on-site generation of renewable energy. For example, a suitable roof pitch can allow installation of thermal solar panels minimise hot water requirements and photovoltaic panels can generate electricity. We try to encourage the most economic type of generation with the latest technology, suitable for the site, which reduces the carbon impact of the building as well as giving economic benefits such as government feed-in-tariffs.</p>
        <p>You can see the above principles  in the design of our<a href="../projects/low_energy_houses/zero-carbon-homes.php" target="_blank"> zero carbon homes</a>.</p>
        <p align="center"><img src="../images/Sustainability/economy-environment.jpg" alt="" width="200" height="120" /></p>
        <p>In our own office we have invested heavily in new technology, while also being as energy efficient as possible to reduce our carbon footprint while also saving money. We have adopted the following measures:</p>
        <p>- Reduced our energy consumption through careful assement of when computers and appliances require power. We make sure every item that is not nessecary, is turned off during evening and weekends.</p>
        <p>- Use very low energy LED lights where possible.</p>
        <p>- Installed triple glazed windows.</p>
        <p>- Changed our energy supplier to  a 100% sustainably sourced company (<a href="http://www.goodenergy.co.uk/">Good Energy</a>)</p>
        <p>- Installed <a href="http://www.solyndra.com/">Solyndra</a> solar PV panels on our roof that have a power of 3.64kW which we hope during the year should go a long way to meeting our consumption requirements. These panels are a clever new design that can sit on a flat roof without fixings. The panels are grid connected so any surlpus energy is exported and so benefits other people. Here is a photo of our roof:</p>
