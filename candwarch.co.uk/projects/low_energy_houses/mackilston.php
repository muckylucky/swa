      <td align="left" valign="top"><div id="copy"><!-- InstanceBeginEditable name="copy" --><p>The house is sited in a natural concave area of hillside facing principally west along 
        the contours to enjoy the spectacular landscape setting of the river Ken valley and 
        the ridges of the Rhinns of Kells hills opposite. <br />
        The intention was to create a contemporary single storey ʻlong houseʼ which is 
        recessive in the landscape, sustainable in its construction, very low in energy 
        consumption, and aiming for zero net emissions of carbon dioxide for all energy use 
        in the house. </p>
            <p>              The design uses lightweight but highly insulated steel and timber frame 
        construction, clad in cedar weatherboarding allowed to weather to a natural silver 
        grey colour. The roof ﬁnish is pre-weathered grey standing seam zinc. Windows 
        and external doors are triple glazed high performance timber, painted grey. All <br />
          insulation levels are to Passiv Haus standards. </p>
            <p> The slope of the roof of the main living accommodation follows the slope of the 
              hillside, with the rear roof meeting the main roof at a shallower angle to allow 
              morning sunlight to penetrate the centre of the house. <br />
              The entrance to the house is sited on the north east side of the house under the 
              cover of the roof to provide shelter from the prevailing wind. The principal rooms 
              are situated along the contour of the site to enjoy the views across the valley to the 
              west. The ancillary spaces are generally to the rear. <br />
              <br />
            </p>
            <p><img src="../../images/projects/low_energy_houses/mackilston/Mackilston-Plan.gif" alt="zero carbon homes" width="599" height="299" /><br />
            </p>
            <p>The house is net ʻzero carbonʼ by using very high levels of insulation, minimising air 
            inﬁltration heating using an air source heat pump with a ʻwhole house heat recovery 
            ventilation systemʼ, and generating electricity using a wind turbine and solar panels.</p>
            <p><strong>What do we mean by zero   carbon houses ?</strong></p>
            <p>Design to  minimise carbon emissions from a   building:</p>
            <ul>
              <li>Ensure energy efficiency.</li>
              <li>Use micro-generation and low or zero carbon energy technologies to move   toward energy self-sufficiency of the building.</li>
            </ul>
            <p> A<strong> </strong>zero carbon house design  will payback the carbon invested in its construction through exporting   zero carbon energy back into the national grid.</p>
            <p>On this site we believe that this definition of a genuine zero carbon house   is the most appropriate. The payback of invested carbon and the aspiration to   move toward negative carbon buildings (buildings that have exported more zero   carbon energy back into the national grid than was expended in construction) are   cornerstones of our zero carbon building definition.</p>
