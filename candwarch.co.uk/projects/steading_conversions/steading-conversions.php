      <p>As agriculture practices evolve and change, traditional stone built farm steadings become obsolete. The long narrow nature of these building types, typically in a sheltered courtyard form are well suited to conversion to dwellings.</p>
    <p>We have designed a number of steading conversions, common themes in our design responses being;</p>
      <p>1. Stripping back insensitive alterations and large span sheds to reveal the existing masonry buildings.<br />
        2. Minimise the visual impact of car parking.<br />
        3. Reuse existing openings and features where appropriate.<br />
        4. Keep new openings to appropriate scales, either small window size or large barn door size.<br />
        5. Position the main rooms in the dwellings to face outwards towards views of surrounding landscape and to enjoy passive solar gain.<br />
        6. New additions to match the form and materials of the existing or contrast where appropriate.<br />
      </p>
      <p>Steading conversions can involve consultation and liaison with local authority planning and building control departments, solicitors and land agents, wildlife consultants and structural engineers. We are fully experienced in this process and can advise potential clients on the likely timescales and costs involved.</p>
