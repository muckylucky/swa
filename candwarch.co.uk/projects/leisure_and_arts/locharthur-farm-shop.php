        <p>Loch Arthur   is a working Community of more than 70 people, including men and women   with learning disabilities. Loch Arthur Creamery and Farm Shop have   grown out of the productive efforts of this diverse and vibrant   Community. The highest standards of Organic production are upheld in   their <a b16f571d4f17="true" rel="nofollow" target="_blank" href="http://www.biodynamic.org.uk/">Demeter Certified</a> Farm and Creamery, the Bakery, and Craft Workshops. The new Farm Shop   was designed in collaboration with Denis Chanarin of Camphill   Architects, an architect and community member. It is based on some of   the precepts of Rudolf Steiner whose philosophy is fundamental to the   community.Â </p>
        <p>A   series of low wings, attached to a higher barn-like centre echoes the   appearance of traditional agricultural buildings. The exterior cladding   of durable Scottish timber boarding will be allowed to weather to a   natural silvery-grey. Structural elements of home grown Douglas Fir are   expressed internally to create interest and give visual warmth and   tactility. A holistic, approach to the building fabric, structure,   materials and services has been adopted to reduce carbon emissions,   resulting in a carbon footprint 40% better than notional figures for   this building type.</p>
        <p>For   all the devoted people who support and shop at Loch Arthur the new   development will provide a warm, social, welcoming, well-stocked and   enjoyable environment for many years to come.</p>
