      <p>This proposal intends to re-use existing ruins to form  a three bedroomed dwelling.
      
      <p>Orientation </p>
      <p>The existing building’s main aspect is south facing and the proposed design conforms to this existing angle of orientation.  Living spaces and larger glazed areas are located to the south to exploit natural light and views towards the open rolling landscape.  A sheltered courtyard is formed to the north as the principle point of arrival and entry to the building.</p>
      <p>Landscape</p>
      <p>The existing site is located on a relatively level plateau, minimizing the extent of excavation required.  An existing dilapidated drystone dyke and a line of existing trees adjacent to the building will be retained.  The land to the south-east of the building will form the client’s private garden.  Areas of adjacent mature oak plantations create a sense of context in the wider landscape and are unaffected by proposals.</p>
      <p>Massing</p>
      <p>The overall mass of the design can be read as a cluster of single storey buildings, minimizing the buildings’ visual impact within its setting.  Where the proposed dwelling projects forward of the existing ruin, it is glazed to visually “step back” in line with the existing principal elevation.  New slated roof slopes will be pitched to match the existing building.</p>
      <p>Materials</p>
      <p>The existing natural stone building will be retained and made good, new parts of the dwelling are differentiated in horizontal cedar weatherboarding allowed to weather to a natural silver grey colour.  The design incorporates very high levels of insulation, increasing energy efficiency, and will aim to be “zero carbon”.  The main roof slopes will be in natural slate, with smaller shallow roof slopes in grey standing seam zinc.</p>
      <p>Inclusive design</p>
      <p>There is a provision for level access via ramp to the north (main) entrance and the majority of the accommodation is provided on one level, with accessible wcs.</p>
      <p>Access </p>
      <p>The proposed access drive will connect to an unnamed road that connects to the B722 to the east.  The route of the access drive will follow the existing farm boundary fence and will be defined by some new deciduous tree planting at regular intervals.</p>
