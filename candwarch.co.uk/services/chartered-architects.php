      <p><img src="../images/profile/rias-logo-600wide.jpg" alt="chartered architects" width="600" height="137" /></p>
      <p><strong>Services we provide</strong></p>
      <p>Architectural Design<br />
        Architectural Drawings <br />
        Master Planning<br />
        Planning Applications<br />
        Planning Appeals<br />
        Listed Building Renovations<br />
        House Inspections<br />
        Feasibility Studies<br />
        Design &amp; Build<br />
        Interior Design<br />
        3D Design Presentation<br />
        Condition Surveys<br />
        Energy Design Analysis<br />
        SAP Calculations<br />
        Energy Performance Certificates<br />
      <p><img src="../images/consultants-links/RIAS-logo.png" alt="rias logo" width="113" height="50" /></p>
      <p>The RIAS guide on 'Why use an architect?&quot;  gives a good explanation of the services and value architects can provide for a project. You can download this document <a href="RIAS-WhyUseACharteredArchitect.pdf">here</a>.</p>
      <p><img src="../images/consultants-links/RIBA_architecture_com2.png" alt="riba logo" width="129" height="50" /></p>
      <p>Also the RIBA guide &quot;Working with an Architect for your home&quot; is a good starting point, found <a href="RIBA-Workingwithanarchitectforyourhome.pdf">here</a>.</p>
      <p><strong>Our Schedule of charges ~ Fees </strong></p>
      <p>Before the fee can be agreed, both architect and client should establish:<br />
        - The project details and services to be provided<br />
        - The procurement method<br />
        - An approximate construction cost<br />
        - The project timetable. </p>
      <p>FEE BASIS<br />
        Our fees are based on skills, experience, overheads and the resources needed to undertake the work. We calculate a total fee to be charged at an agreed percentage of the construction costs. </p>
      <p>This percentage we have based on:<br />
        - The classification of the building (whether new build or works to existing).<br />
        - The size of the project.  (The larger the project the smaller the percentage).<br />
        - The site constraints and complexities.  (A simple, flat, serviced plot of land requires less work)<br />
        - The nature of the design specification.  (A higher design spec that will be much more unique, needs more time spent on selection with client, technical research and communication with contractor to get the best possible solution &amp; finish).</p>
      <p>In the early stages the construction costs are based on:<br />
        - All of the above.<br />
        - An approximate rate per square meter of internal floor area of the proposed design.<br />
        - Any additional information from the client about budget.<br />
        - Any budget quotation given by contractors, sub contractors, consultants or suppliers.</p>
      <p>At the later stages of the project when the construction costs are more known, the figures are based on the total value of all works within the project site.  This may include tender accepted main contract and sub-contractors figures.</p>
      <p>In 2006, an independent annual survey of fees by Mirza &amp; Nacey Research recorded the general level of architect’s fees for providing a full service for mostly new private houses to be between 8% and 12% of the construction cost.  On projects to refurbish or extend houses,
        or for repair and conservation work on historic buildings, the fee can be a larger percentage of the building cost.</p>
      <p>FEE SCHEDULE<br />
        This total fee is broken into stages related to the RIBA work stages as shown below:</p>
      <p>Appraisal<br />
        RIBA Stage AB <br />
        Initial consultation and site visit to determine project feasibility, usually charged as an hourly rate but deducted from account if the project goes ahead and we are instructed. Invoiced after client meeting.</p>
      <p>Outline Proposals<br />
        RIBA Stage C<br />
        Initial sketch design with outline drawing to meet clients brief.  To commence work a measured survey will need to be carried out by a consultant which is an additional cost. Invoiced after sign off from client. </p>
      <p>Detailed Proposals<br />
        RIBA Stage D <br />
        Design for full planning permission submitted and local authority queries dealt with.   At this stage there will be an additional cost for the Planning application fee and may be costs for extra time spent if 3D visualisation services are required for marketing or planning purposes.<br />
        Invoiced after submission to council.</p>
      <p>Final  Proposals<br />
        RIBA Stage E <br />
        Building warrant submission for building regulation approval.  At this stage there will be an additional cost for a SAP calculation (energy use analysis) and the warrant application fee.  Invoiced after submission to council.</p>
      <p>Tender<br />
        RIBA Stage FG <br />
        Produce drawings and specification for project to be priced by contractors. Invoiced after contractor chosen.</p>
      <p>Building Contract<br />
        RIBA Stage HJKL <br />
        Organise, detail and supervise all building work to achieve satisfactory completion.  Invoiced at monthly stages throughout the building process at a timescale agreed with client and builder based on schedule of works.</p>
      <p>As each project is unique, we are happy to discuss your specific needs if you <a href="../contact.php">contact us</a> via phone or email initially and we can arrange a visit to your site for an appraisal if necessary.</p>
