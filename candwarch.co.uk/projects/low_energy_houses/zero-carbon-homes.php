        <p><strong>Our Philosophy - Zero Carbon Homes </strong></p>
        <p>As we start the 2nd decade of the 21st century mankind is becoming increasingly aware of the fragility of the planet’s ecosystem and the urgent need to take steps to reduce the causes of global warming. This occurs in parallel to the growing cost of energy and demand on global resources that reinforces the argument for investing in sustainable, green technology.
          </p>
        <p>Our practice is conscious of the role we can play in setting an example of energy efficient houses and sustainable buildings which aim to address these global issues by designing for the future.          </p>
        <p>In this regard we have been designing homes which meet the Government’s aim for Zero Carbon Homes now and for the last few years, even though the Government’s target is to achieve this by 2016. We aim to demonstrate that this target can be achieved economically without significant additional build costs by becoming energy efficient and applying the appropriate use of green technology.</p>
          <p>There are a number of ways to achieve energy efficient buildings that are outlined in the examples of buildings in this section. The most energy efficient building is one designed to Passive House Standards and is a net zero carbon house.</p>
          <p><img src="../../images/zero-carbon-diagram.jpg" width="600" height="400" alt="zero carnon house design" /></p>
          <p><strong>Passive House Design </strong></p>
          <p>A Passive House design is one that is designed to a high enough standard to reduce the energy consumption enough to relinquish the need for a conventional heating system. On average a Passive house will require between 70 and 90% less energy than a house built to the minimum requirements for building regulations. Combining this principle of reduced consumption with the generation of green electricity can effectively give a building a net Zero Carbon status.</p>
            <p>This is achieved in a number of ways:</p>
            1. Orientation / passive solar gain.<br />
            2. Super insulating the walls, floor and roof.<br />
            3. Increased air tightness to minimise the amount of warm air escaping the building.<br />
            4. Good thermal mass to retain heat in the building. <br />
            5. Using a mechanical heat recovery ventilation system to maintain air quality and retain 80% of internally dispensed heat.<br />
            6. Advanced window technology with low e, gas filled, triple glazing to achieve greater heat gains through the sun than the heat losses.<br />
            7. Renewable energy space and water heating such as solar water heating and ground source heat pumps.<br />
            8. Renewable energy power generation such as wind turbines, solar PV panels and micro hydro systems</p>
        <p>We believe that an <a href="ford-house.php">energy efficient house</a>, or Passive house not only means less impact on the environment by decreasing the impact on global warming but also gives a better quality of life with fresh, clean air, a consistent internal temperature, a more open living space with underfloor heating and a massively reduced running cost. </p>
