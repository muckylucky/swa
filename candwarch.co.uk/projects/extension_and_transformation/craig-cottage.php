        <p>Craig Cottage is a 19th century fisherman's cottage on a spectacular coastal site near Borgue in Dumfries and Galloway. The clients came to us with an ambitious brief which involved more than doubling the footprint of the existing cottage. The existing cottage was pared back by removing the existing ad hoc extensions, to reveal the original cottage shell, forming an extension which mirrors the form of the existing cottage but in modern timber frame construction and clad in western red cedar cladding which will weather naturally and appear recessive against the whitewashed cottage. The new addition mimics the form of the existing building but using contemporary materials and window treatments. An open plan kitchen and sunroom forms the central hub of the house. The objective is to transform a modest traditionally built house into a modern home which performs equally well to a modern new build in terms of energy efficiency, incorporating an air source heat pump, whole house heat recovery ventilation as well as maximising passive energy benefits through correct orientation. </p>
